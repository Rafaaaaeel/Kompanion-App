import Foundation

public enum FontSize {
    
    public enum Header: CGFloat {
        case h1 = 96
        case h2 = 32
        case h3 = 24
        case h4 = 16
        case h5 = 18
    }
    
    public enum Paragraph: CGFloat {
        case p12 = 12
        case p14 = 14
        case p16 = 16
    }
    
}
